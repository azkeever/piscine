char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i] && str[i] >= ' ' && str[i] <= '~')
	{
		if (str[i - 1] >= 'A' && str[i - 1] <= 'Z')
			;
		else if (str[i -1] >= 'a' && str[i - 1] <= 'z')
			;
		else if (str[i - 1] >= '0' && str[i - 1] <= '9')
			;
		else
			str[i] -= 32;
		i++;
	}
	return (str);
}
